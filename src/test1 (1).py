"""# Extract frames from a video and compute embeddings using DeepFace
- Frames are extracted every N frames to reduce redundancy.
- Each frame is passed through DeepFace (Facenet) to compute 128-d embeddings.
- Aggregated embeddings are averaged per student enrollment.

"""

def extract_frames_from_video(video_path: str, frame_interval: int = 5) -> List[np.ndarray]:
    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        raise IOError(f"Cannot open video file {video_path}")
    frames = []
    idx = 0
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        if idx % frame_interval == 0:
            frames.append(frame.copy())
        idx += 1
    cap.release()
    return frames

def frames_to_embeddings(frames: List[np.ndarray]) -> np.ndarray:
    embeddings = []
    for frame in frames:
        try:
            emb = DeepFace.represent(frame,model_name="Facenet", detector_backend="mtcnn", enforce_detection=True)
            embeddings.append(np.array(emb[0]["embedding"]))
        except:
            pass
    if not embeddings:
        return np.empty((0,128))
    return np.vstack(embeddings)

