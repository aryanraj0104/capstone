"""##**Approach 2**
## Cosine Matching: Recognition Using Mean Embeddings


- Instead of training an SVM, we compute a mean embedding per student from all their frames in the enrollment video.

- To recognize a new image: compute its embedding and compare it with all mean embeddings using cosine distance.

- **Threshold**: Determines how similar the embeddings need to be to consider a match. A lower threshold = stricter match (less false positives), a higher threshold = looser match (may accept similar but different faces).

This method works well for small datasets and can scale to thousands of students later, as we only compare vectors, no classifier training required.
"""

