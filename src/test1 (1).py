from scipy.spatial.distance import cosine
def recognize_with_means(image_path, threshold=0.6):
    img = cv2.imread(image_path)
    try:
        emb_array = frames_to_embeddings([img])
        if emb_array.shape[0] == 0:
            print("No face detected in the image.")
            return
    except Exception as e:
        print(f"Error processing image: {e}")
        return

    enroll_files = sorted(list(WORKDIR.glob("*.npy")))
    X_mean_list, y_mean_list = [], []
    label_map_mean = {}
    for idx, f in enumerate(enroll_files):
        mean_emb = np.load(f)
        X_mean_list.append(mean_emb)
        y_mean_list.append(idx)
        label_map_mean[idx] = f.stem

    X_mean = np.vstack(X_mean_list)
    scores = [cosine(emb_array[0], mean) for mean in X_mean]
    best_idx = np.argmin(scores)
    best_score = scores[best_idx]

    if best_score > threshold:
        print("No match found. Closest score exceeds threshold.")
        return

    roll_pred = label_map_mean[best_idx]
    print(f"Predicted roll: {roll_pred} (cosine distance: {best_score:.4f})")
    return roll_pred


