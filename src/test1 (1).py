"""# Build dataset for training
- Load all saved `.npy` files from `enrollments/`.
- Each student's embeddings are labeled with their roll number.
- Output: X (N×128 embeddings), y (labels), label_map (int->roll mapping)


"""

def find_enrollment_files(enroll_dir: Path = WORKDIR) -> List[Path]:
    return sorted(list(enroll_dir.glob("*.npy")))

def build_dataset_from_npy_files(enroll_files: List[Path]) -> Tuple[np.ndarray, np.ndarray, Dict[int,str]]:
    X_parts, y_parts = [], []
    label_map = {}
    current_label = 0
    for f in enroll_files:
        roll = f.stem
        encs = np.load(f)
        if encs.size == 0:
            continue
        label_map[current_label] = roll
        X_parts.append(encs)
        y_parts.append(np.full((encs.shape[0],), current_label, dtype=int))
        current_label += 1
    if not X_parts:
        return np.empty((0,128)), np.array([], dtype=int), label_map
    X = np.vstack(X_parts)
    y = np.concatenate(y_parts)
    return X, y, label_map

